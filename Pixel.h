

class Pixel{
    //Atributes
    int redValue, greenValue,blueValue;

    //Constructor

    public:
        Pixel(){
            this->redValue = 0;
            this->greenValue = 0;
            this->blueValue = 0;
        }


        Pixel(int pRedValue,int pGreenValue,int pBlueGreen){

            this->redValue = pRedValue;
            this->greenValue = pGreenValue;
            this->blueValue = pBlueGreen;

        }
    

};